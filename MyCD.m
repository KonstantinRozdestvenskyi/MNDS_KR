    %Pecora-Carroll synchronization

    Y = X;
    
    h1 = h * 0.5; 
    h2 = h * (1 - 0.5);
    % X(1) = X(1) + h1 * ( a(1) * ( X(2) - X(1))) ;
    % X(2) = X(2) + h1 * ( X(1) * ( a(2) - X(3)) - X(2)) ;
    % X(3) = X(3) + h1 * ( X(1) * X(2) - a(3) * X(3)) ;

    X(1) = X(1) + h1 * (a(1) * X(2) * X(4));
    X(2) = X(2) + h1 * (a(6) * X(2) * X(3));
    X(3) = X(3) + h1 * (-a(2) * (X(2))^2 + a(3) * X(4));
    X(4) = X(4) + h1 * (-a(4) * X(1) * X(2) - a(5) * (X(3))^3);
    
    % X(3) = (X(3) + h2 * ( X(1) * X(2)))/(1 + h2*a(3)) ;
    % X(2) = (X(2) + h2 * ( X(1) * ( a(2) - X(3))))/(1 + h2) ;
