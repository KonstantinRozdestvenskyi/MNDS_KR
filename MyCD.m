    X(4) = X(4) + h2 * (-a(4) * X(1) * X(2) - a(5) * (X(3))^3);
    X(3) = X(3) + h2 * (-a(2) * (X(2))^2 + a(3) * X(4));
    X(2) = X(2)/(1 - h2 * a(6) * X(3));
    X(1) = X(1) + h2 * (a(1) * X(2) * X(4));

    
    
    Y(1) = X(1);
    Y(2) = X(2);
    Y(3) = X(3);
    Y(4) = X(4);
end